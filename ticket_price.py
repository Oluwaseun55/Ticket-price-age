def code_like_yours(age):
    if age >= 18:
        return "Full price ticket: $12"
    else:
        return "Child discount: $5"


with gr.Blocks(css=custom_css, title="Ticket Price Checker") as demo:
    gr.Markdown("#Ticket Price Checker")
    gr.Markdown("Enter your age to find out your ticket price:")

    age = gr.Number(label="Enter Age", value=0)
    result = gr.Textbox(label="Result", placeholder="Your ticket price will appear here...")
    submit = gr.Button("Check Price")
    submit.click(fn=code_like_yours, inputs=age, outputs=result)

if __name__ == "__main__":
    demo.launch()

